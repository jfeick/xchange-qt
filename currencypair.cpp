CurrencyPair::CurrencyPair()
    : baseCurrency_(""), counterCurrency_("")
{}

CurrencyPair::CurrencyPair(String baseCurrency)
    : baseCurrency_(baseCurrency), counterCurrency_(Currencies::USD)
{}

CurrencyPair::CurrencyPair(String baseCurrency, String counterCurrency)
    : baseCurrency_(baseCurrency), counterCurrency_(counterCurrency)
{}

String
CurrencyPair::toString() const
{
    return (baseCurrency_ + "/" + counterCurrency_);
}

bool
CurrencyPair::operator==(CurrencyPair const& other) const
{
    return(other.baseCurrency_     == baseCurrency_ &&
           other.counterCurrency_  == counterCurrency_);
}

